fn main() {
    // print text without line breacks
    print!("Hello, Maria!"); // Correto

    // print text with line breaks;
    println!("Hello, Carlos!"); // Correto

    // Line break: \n // Correto
    println!("What is your name? \\nMy name is Rodolfo!");

    // Line break: \n
    println!("What is your name? \nMy name is Rodolfo!");

    let a = 5.0;
    let b = 2.0;
    println!("{:?}", a / b);
}
